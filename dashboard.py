    pillar_pills = "".join(
        f'<span class="pillar-pill" style="border-color:{PILLAR_COLOR.get(p,"#94a3b8")}55;'
        f'color:{PILLAR_COLOR.get(p,"#94a3b8")}">{p} <b>{c}</b></span>'
        for p, c in sorted(counts.items(), key=lambda x: -x[1])
    )

