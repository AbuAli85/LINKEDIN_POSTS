# Engagement sections (unchanged API surface, updated CSS classes)
# ---------------------------------------------------------------------------
def _engagement_sections() -> str:
    try:
        from engagement import get_repeat_engagers, load_all_engagement
        items    = load_all_engagement()
        engagers = get_repeat_engagers()
    except Exception:
        return ""

    RISK_COLOR   = {"safe": "#2a9a5c", "review": "#d4840a", "block": "#e8372a"}
    STATUS_COLOR = {"pending": "#d4840a", "approved": "#818cf8", "posted": "#2a9a5c", "blocked": "#e8372a"}

    pending       = [d for d in items if d.get("status") in ("pending", "approved")]
    recent_posted = [d for d in items if d.get("status") == "posted"][:5]

    if not pending:
        pending_html = '<div class="empty" style="padding:30px 20px">No pending replies</div>'
    else:
        cards = []
        for d in pending:
            risk    = d.get("risk_level", "review")
            status  = d.get("status", "pending")
            rc      = RISK_COLOR.get(risk, "#94a3b8")
            sc      = STATUS_COLOR.get(status, "#94a3b8")
            rec     = d.get("recommended_reply", 0)
            comment = html.escape(d.get("comment_text", "")[:200])
            topic   = html.escape(d.get("post_topic", ""))
            fname   = html.escape(Path(d.get("post_file", "engagement_history/?.json")).name)
            reply_rows = ""
            for i, r in enumerate(d.get("reply_drafts", [])):
                star = "&#9733; " if i == rec else ""
                reply_rows += (
                    f'<div class="reply-opt{"rec" if i==rec else ""}">'
                    f'<span class="reply-idx">{star}[{i}]</span> {html.escape(r)}</div>'
                )
            risk_cats = ", ".join(d.get("risk_categories", [])) or "none"
            risk_note = html.escape(d.get("risk_reason", ""))
            cards.append(f"""
            <div class="eng-card">
              <div class="eng-header">
                <span class="eng-badge" style="background:rgba(0,0,0,.3);color:{sc};border:1px solid {sc}44">{status.upper()}</span>
                <span class="eng-badge" style="background:rgba(0,0,0,.3);color:{rc};border:1px solid {rc}44">risk: {risk}</span>
                <span class="eng-meta">{topic[:55]}</span>
              </div>
              <div class="eng-comment">&#128172; {comment}</div>
              {f'<div class="eng-risk-note">&#9888; {html.escape(risk_cats)} &mdash; {risk_note}</div>' if risk=="review" else ""}
              <div class="eng-replies">{reply_rows or "<em>No reply drafts.</em>"}</div>
              <div class="eng-cmd"><code>python engagement.py approve {fname} --reply {rec}</code></div>
            </div>""")
        pending_html = "".join(cards)

    recent_html = ""
    if recent_posted:
        rp_rows = "".join(
            f'<div class="rp-item">&#10003; {html.escape(d.get("posted_reply","")[:100])}</div>'
            for d in recent_posted
        )
        recent_html = f'<h2 class="section-lbl" style="margin-top:22px">Recently posted replies ({len(recent_posted)})</h2>{rp_rows}'

    outreach_html = ""
    if engagers:
        lead_rows = "".join(
            f'<div class="lead-row">'
            f'<span class="lead-name">{html.escape(l.get("commenter_name",""))}</span>'
            f'<span class="lead-count">{l.get("post_count",0)} posts engaged</span>'
            f'<span class="lead-comment">{html.escape((l.get("comments") or [{}])[-1].get("comment_text","")[:80])}</span>'
            f'</div>'
            for l in engagers[:10]
        )
        outreach_html = (
            f'<h2 class="section-lbl" style="margin-top:28px">Outreach leads &mdash; repeat engagers ({len(engagers)})</h2>'
            f'<div class="leads-table">{lead_rows}</div>'
        )

    return f"""
    <div class="content" style="margin-top:0">
      <h2 class="section-lbl">Pending replies ({len(pending)})</h2>
      {pending_html}
      {recent_html}
      {outreach_html}
    </div>"""


