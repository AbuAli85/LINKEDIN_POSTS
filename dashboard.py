CRON_WEEKDAYS  = {5, 0, 2}   # Sat=5, Mon=0, Wed=2
CRON_HOUR_UTC  = 5
MUSCAT_OFFSET  = 4


def _to_muscat(dt: datetime) -> str:
    m = dt + timedelta(hours=MUSCAT_OFFSET)
    return (
        m.strftime("%b %d, %Y · %I:%M %p Muscat")
        .replace(" 0", " ")
        .replace("AM", "am")
        .replace("PM", "pm")
    )


def load_posts() -> list[dict]:
    files = sorted(HISTORY_DIR.glob("*.json"), reverse=True)
    posts = []
    for f in files:
        try:
            p = json.loads(f.read_text(encoding="utf-8"))
            p["_filename"] = f.name
            posts.append(p)
        except Exception:
            continue
    return posts


def next_runs(n: int = 3) -> list[datetime]:
    now = datetime.now(timezone.utc)
    dt  = now.replace(hour=CRON_HOUR_UTC, minute=0, second=0, microsecond=0)
    if dt <= now:
        dt += timedelta(days=1)
    result = []
    for _ in range(14):
        if dt.weekday() in CRON_WEEKDAYS:
            result.append(dt)
            if len(result) >= n:
                break
        dt += timedelta(days=1)
    return result


def _token_health(posts: list[dict]) -> tuple[str, str]:
    published = [p for p in posts if p.get("published") and p.get("published_at")]
    if not published:
        return ("LinkedIn tokens expire after 60 days. Set a reminder to renew.", "info")
    newest = max(published, key=lambda p: p.get("published_at", ""))
    try:
        created   = datetime.fromisoformat(newest["published_at"].replace("Z", "+00:00"))
        expires   = created + timedelta(days=60)
        days_left = (expires - datetime.now(timezone.utc)).days
        if days_left <= 0:
            return ("Token has likely expired — renew immediately (see LINKEDIN_SETUP.md)", "error")
        if days_left <= 10:
            return (f"Token expires in ~{days_left} days — renew now", "error")
        if days_left <= 20:
            return (f"Token expires in ~{days_left} days — plan renewal soon", "warn")
        return (f"Token valid for ~{days_left} more days", "ok")
    except Exception:
        return ("", "")


